		What is Snackfeed? <span class="small-detail"> the lowdown.. </span>
	</div>
	
	<div class="indent-column text-style-1"  >
	



<blockquote>

Snackfeed is a tool that lets you track your favorite videos and watch what your friends are watching.

</blockquote>
	
Actually its much more than that, at least we want it to be! Soon you'll be able to follow all the video activities of your friends - if they upload, blog about or share videos you'll receive updates through your Feed. Likewise your video activities will be published to your friends' Feeds (of course you'll have full control of what others see through your privacy settings).

Please take some time to play around with our site. Find your favorite shows and watch a couple episodes. Invite some friends and start to follow them, or just follow us for now. Drop us a note to say hi or tell us how we can do things better (or differently). It can get kind of lonely over here at Snackfeed Headquarters so we are constantly refreshing our inboxes waiting to hear from you. But hey, no pressure!


	</div>
	<div class="header-big">
		Why Snackfeed? <span class="small-detail"> </span>
	</div>
	
	<div class="indent-column text-style-1"  >



Because Snackfeed is the quickest way to gain street cred.



Who are we kidding, we actually had to google the term "street cred"! Here's the real deal: navigating the web for video is difficult. There are a ton of sites out there if you know exactly what you are looking for. But what if you want to find something interesting, exciting and new? We've found that people, not complex recommendation algorithms, are the best filters for video content. Our friends are a lot cooler than we are and they are usually doing some awesome stuff with video. Snackfeed is a way for you to stay connected to these cool friends and share some of your awesome videos with them.


	</div>
	<div class="header-big">
		How does it work? <span class="small-detail">Its really simple actually. </span>
	</div>
	
	<div class="indent-column text-style-1"  >



First create an account using your email address. Still with us? You will be taken to your Feed, where you can choose shows or people to follow. By default you will be following us - CC, Mika, Jason, Kortina - but you can change that through your settings (don't worry, we won't be upset). In your settings you can find your friends or invite them to join. You will also be able to link your other accounts so that your video activities can be published to your feeds. Thats it, you're done! Now sit back, grab some popcorn, and snack on some videos.


